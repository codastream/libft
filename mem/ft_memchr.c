#include "../libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*s_cast;
	size_t			i;

	s_cast = (unsigned char *) s;
	i = 0;
	while (i < n)
	{
		if (s_cast[i] == (unsigned char) c)
			return (&s_cast[i]);
		i++;
	}
	return (NULL);
}
