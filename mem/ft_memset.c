#include "../libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*s_cast;
	size_t			i;

	s_cast = (unsigned char *) s;
	i = 0;
	while (i < n)
	{
		s_cast[i] = (unsigned char) c;
		i++;
	}
	return (s);
}
